CREATE OR REPLACE VIEW V_TOP_PLAYERS AS
SELECT 
    U.ID,
    U.USERNAME,
    COUNT(DISTINCT GS.ID) AS TOTAL_GAMES,
    SUM(CASE WHEN GST.NAME = 'solved' THEN 1 ELSE 0 END) AS SOLVED_GAMES,
    ROUND(
        SUM(CASE WHEN GST.NAME = 'solved' THEN 1 ELSE 0 END) / 
        NULLIF(COUNT(DISTINCT GS.ID), 0) * 100, 2
    ) AS SUCCESS_RATE,
    AVG(
        CASE WHEN GST.NAME = 'solved'
             THEN EXTRACT(HOUR FROM (GS.END_TIME - GS.START_TIME)) * 60 + 
                  EXTRACT(MINUTE FROM (GS.END_TIME - GS.START_TIME))
        END
    ) AS AVG_TIME_MINUTES,
    MIN(
        CASE WHEN GST.NAME = 'solved' 
             THEN GS.STEPS_COUNT 
        END
    ) AS BEST_STEPS
FROM USERS U
LEFT JOIN GAME_SESSIONS GS ON U.ID = GS.USER_ID
LEFT JOIN GAME_STATUSES GST ON GS.STATUS_ID = GST.ID
WHERE (GST.NAME != 'active' OR GST.NAME IS NULL)
GROUP BY U.ID, U.USERNAME;


-- V_TOP_PLAYERS


CREATE OR REPLACE VIEW V_TOP_PLAYERS AS
SELECT
    U.ID,
    U.USERNAME,
    COUNT(DISTINCT GS.ID) AS TOTAL_GAMES,
    SUM(CASE WHEN GST.NAME = 'solved' THEN 1 ELSE 0 END) AS SOLVED_GAMES,
    ROUND(
        SUM(CASE WHEN GST.NAME = 'solved' THEN 1 ELSE 0 END) /
        NULLIF(COUNT(DISTINCT GS.ID), 0) * 100, 2
    ) AS SUCCESS_RATE,
    ROUND(AVG(
        CASE WHEN GST.NAME = 'solved'
             THEN (GS.END_TIME - GS.START_TIME) * 24 * 60
        END
    ), 2) AS AVG_TIME_MINUTES,
    MIN(
        CASE WHEN GST.NAME = 'solved'
             THEN GS.STEPS_COUNT
        END
    ) AS BEST_STEPS
FROM USERS U
LEFT JOIN GAME_SESSIONS GS ON U.ID = GS.USER_ID
LEFT JOIN GAME_STATUSES GST ON GS.STATUS_ID = GST.ID
WHERE (GST.NAME != 'active' OR GST.NAME IS NULL)
GROUP BY U.ID, U.USERNAME;


--V_GAME_STATUS

CREATE OR REPLACE VIEW V_GAME_STATUS AS
SELECT
    GS.ID AS SESSION_ID,
    U.USERNAME,
    PS.GRID_SIZE,
    DL.NAME AS DIFFICULTY,
    GST.NAME AS STATUS,
    GS.STEPS_COUNT AS TOTAL_STEPS,
    GA.UNDO_POINTER AS CURRENT_STEP,
    GA.CURRENT_MISPLACED_TILES AS MISPLACED_TILES,
    GA.CURRENT_MANHATTAN_DISTANCE AS MANHATTAN_DISTANCE,
    GA.INITIAL_MANHATTAN_DISTANCE AS INITIAL_MANHATTAN,
    CASE
        WHEN GA.INITIAL_MANHATTAN_DISTANCE > 0
        THEN ROUND(
            (GA.INITIAL_MANHATTAN_DISTANCE - GA.CURRENT_MANHATTAN_DISTANCE) /
            GA.INITIAL_MANHATTAN_DISTANCE * 100, 1
        )
        ELSE 100
    END AS PROGRESS_PCT,
    ROUND((SYSDATE - GS.START_TIME) * 24 * 60, 1) AS ELAPSED_MINUTES,
    GA.TIME_LIMIT,
    GS.START_TIME,
    GS.LAST_ACTIVITY_AT
FROM GAME_SESSIONS GS
JOIN USERS U ON GS.USER_ID = U.ID
JOIN GAME_STATUSES GST ON GS.STATUS_ID = GST.ID
JOIN PUZZLES PZ ON GS.PUZZLE_ID = PZ.ID
JOIN PUZZLE_SIZES PS ON PZ.PUZZLE_SIZE_ID = PS.ID
JOIN DIFFICULTY_LEVELS DL ON PZ.DIFFICULTY_ID = DL.ID
JOIN GAME_ATTEMPTS GA ON GA.SESSION_ID = GS.ID
    AND GA.STATUS_ID = (SELECT ID FROM GAME_STATUSES WHERE NAME = 'active')
WHERE GST.NAME = 'active';


-- V_BOARD

